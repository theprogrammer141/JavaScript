let grade=prompt("Enter a grade:");
if(grade >= 80 && grade <= 100)
{
    console.log("A");
}
else if(grade >=70 && grade <= 89)
{
    console.log("B");
}
else if(grade >= 60 && grade <=69)
{
    console.log("C");
}
else if(grade >=50 && grade <=59)
{
    console.log("D");
}
else
{
    console.log("F");
}